<?php 

# pi() 
echo (pi()); // return 3.14159
echo "\n";



# min() max() 
echo (min(0, 150, 30, 20, -8, -200)); // return -200
echo "\n";
echo (max(0, 150, 30, 20, -8, -200)); // return 150
echo "\n";



# abs() 
echo (abs(-6.7)); // return 6.7
echo "\n";



# sqrt() 
echo (sqrt(64)); // return 8
echo "\n";



# round() 
echo (round(0.60)); // return 1
echo "\n";
echo (round(0.49)); // return 0
echo "\n";



# rand()
echo (rand()); // rastgele sayı üretir
echo "\n";
echo (rand(10, 100)); // 10 ile 100 arasında rastgele sayı üretir
echo "\n";
?>
