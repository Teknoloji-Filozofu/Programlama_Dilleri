void main() {
  int a = 10, b = 5;

  if (a > b)
    print("\n a değeri b' den büyüktür. \n");
  else
    print("\n a değeri b'den küçük yada eşittir. \n");

  if (a >= b)
    print("\n a değeri b'den büyük yada eşittir. \n");
  else
    print("\n a değeri b'den küçüktür. \n");

  if (a < b)
    print("\n a değeri b'den küçüktür. \n");
  else
    print("\n a değeri b'den büyüktür yada eşittir. \n");

  if (a <= b)
    print("\n a değeri b'den küçük yada eşittir. \n");
  else
    print("\n a değeri b'den büyüktür. \n");

  if (a == b)
    print("\n a değeri b'ye eşittir. \n");
  else
    print("\n a değeri b'ye eşit değildir. \n");

  if (a != b)
    print("\n a değeri b'ye eşit değildir. \n");
  else
    print("\n a değeri b'ye eşittir. \n");
}
