void main() {
  int a, b, c, d, e, g, h;
  double f;

  a = 10;
  b = 5;

  //toplama işlemi
  c = a + b;
  print(c);

  //çıkarma işlemi
  d = a - b;
  print(d);

  //çarpma işlmei
  e = a * b;
  print(e);

  //bolme işlemi
  f = a / b;
  print(f);
  // yuvarlama işlmei yaparak cevabı döndür
  h = a ~/ b;
  print(h);

  //mod alma işlemi
  g = a % b;
  print(g);

  //1 arttırma işlmei
  c++;
  print(c);

  // 1 eksiltme işmei
  c--;
  print(c);
}
